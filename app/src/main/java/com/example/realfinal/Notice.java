package com.example.realfinal;

public class Notice {

    String notice;
    String name;


    public Notice(String notice, String name) {
        this.notice = notice;
        this.name = name;

    }

    public String getNotice() {
        return notice;
    }

    public void setNotice(String notice) {
        this.notice = notice;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


}
